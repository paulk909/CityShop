    public StaffViewOrder(Staff staff) throws SQLException {
        try {
            LoggedInStaff = staff;
            DBHandler db = new DBHandler();
            customers = db.loadCustomers();
            
            initComponents();
            
            tblOrders.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            
            DefaultTableModel model = (DefaultTableModel)tblOrders.getModel();
            for(Map.Entry<String, Customer> keeperEntry : customers.entrySet())
            {
                Customer acc = keeperEntry.getValue();
                for(Map.Entry<Integer, Order> orderEntry : acc.getCompleteOrders().entrySet())
                {
                    Order ao = orderEntry.getValue();
                    
                    if(ao.getStatus().equals("Complete"))
                    {
                        model.addRow(new Object[]{acc.getUsername(),
                            ao.getOrderID(), ao.getOrderDate(),
                            "Â£" + String.format("%.02f",ao.getOrderTotal())});
                    }
                }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(StaffViewOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

